package q2;

public class FinalScore {
	private String studentId;
	private double score;
	
	public FinalScore(String studentId, double score) {
		this.studentId = studentId;
		this.score = score;
	}
	
	public String getStudentId() {
		return studentId;
	}
	public double getScore() {
		return score;
	}
}
